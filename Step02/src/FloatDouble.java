
public class FloatDouble {

	public static void main(String[] args) {
		float f1 = 3.1415f;
		double d1 = 3.1415;
		
		System.out.println(f1);
		System.out.println(d1);
		
		//원 넓이
		int r = 3;
		//f1을 이용해서 원넓이 출력
		System.out.println(r*r*f1);
		//d1을 이용해서 원넓이 출력
		System.out.println(r*r*d1);
		
	}

}
