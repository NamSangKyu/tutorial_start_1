
public class Operator1 {
	/*
	 * 		사칙 연산자
	 * 		+	- 	*	/	%
	 */
	public static void main(String[] args) {
		//정수형 변수 두개 선언, 값은 10, 3으로 초기화
		int num1 = 10, num2 = 3;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2); //몫
		System.out.println(num1 % num2); //나머지
	}

}
