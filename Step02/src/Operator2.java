
public class Operator2 {
	/*
	 * 		복합대입연산자
	 * 		+=	-=	*=	/=	%=
	 */
	public static void main(String[] args) {
		int num = 10;
		num += 10;//num = num + 10;
		System.out.println(num);
		num -= 3;//17
		System.out.println(num);
		num *= 3;//51
		System.out.println(num);
		num /= 4;//12
		System.out.println(num);
		num %= 4;//0
		System.out.println(num);
	}

}
